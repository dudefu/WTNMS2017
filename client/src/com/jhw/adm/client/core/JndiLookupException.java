package com.jhw.adm.client.core;

public class JndiLookupException extends ApplicationException {


	public JndiLookupException() {
		super();
	}

	public JndiLookupException(String message) {
		super(message);
	}

	public JndiLookupException(String message, Throwable cause) {
		super(message, cause);
	}

	public JndiLookupException(Throwable cause) {
		super(cause);
	}
	
	private static final long serialVersionUID = 3147033655652973194L;
}
